import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { taxonomyV3Source, TAXONOMY_V3_VERSION } from './taxonomy-v3-source.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const OUTPUT_PATH = path.resolve(__dirname, '../prisma/taxonomy-v3.sql');
const MIN_RULES_PER_CATEGORY = 100;
const MAX_RULES_PER_CATEGORY = 150;

function slugify(value) {
  return value
    .toLowerCase()
    .replaceAll("'", '')
    .replaceAll('+', 'plus')
    .replaceAll('.', '-')
    .replaceAll('/', '-')
    .replaceAll(' ', '-')
    .replace(/[^\p{L}\p{N}_-]+/gu, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
}

function sqlEscape(value) {
  return value.replaceAll("'", "''");
}

function pushRule(target, seen, rule) {
  const dedupeKey = [rule.category, rule.lang, rule.matchType, rule.pattern.toLowerCase()].join('|');

  if (seen.has(dedupeKey)) {
    return;
  }

  seen.add(dedupeKey);
  target.push(rule);
}

function expandTemplates(terms, templates) {
  const results = [];

  for (const template of templates) {
    for (const term of terms) {
      results.push(template.replaceAll('{term}', term));
    }
  }

  return results;
}

function buildRulesForLanguage(definition, lang) {
  const rules = [];
  const seen = new Set();
  const keywords = lang === 'zh-CN' ? definition.zhKeywords : definition.enKeywords;
  const phrases = lang === 'zh-CN' ? definition.zhPhrases : definition.enPhrases;
  const weak = lang === 'zh-CN' ? definition.zhWeak : definition.enWeak;
  const regexes = lang === 'zh-CN' ? definition.zhRegex : definition.enRegex;
  const expansionTerms = lang === 'zh-CN' ? definition.zhExpansionTerms : definition.enExpansionTerms;
  const expansionTemplates = lang === 'zh-CN' ? definition.zhExpansionTemplates : definition.enExpansionTemplates;
  const label = lang === 'zh-CN' ? definition.labelZh : definition.labelEn;

  for (const keyword of keywords) {
    pushRule(rules, seen, {
      category: definition.category,
      label,
      lang,
      matchType: 'keyword',
      pattern: keyword,
      weight: 5,
    });
  }

  for (const phrase of phrases) {
    pushRule(rules, seen, {
      category: definition.category,
      label,
      lang,
      matchType: 'phrase',
      pattern: phrase,
      weight: 7,
    });
  }

  for (const hint of weak) {
    pushRule(rules, seen, {
      category: definition.category,
      label,
      lang,
      matchType: 'keyword',
      pattern: hint,
      weight: 3,
    });
  }

  for (const regex of regexes) {
    pushRule(rules, seen, {
      category: definition.category,
      label,
      lang,
      matchType: 'regex',
      pattern: regex,
      weight: 7,
    });
  }

  for (const term of expansionTerms) {
    pushRule(rules, seen, {
      category: definition.category,
      label,
      lang,
      matchType: 'keyword',
      pattern: term,
      weight: 5,
    });
  }

  for (const phrase of expandTemplates(expansionTerms, expansionTemplates)) {
    pushRule(rules, seen, {
      category: definition.category,
      label,
      lang,
      matchType: 'phrase',
      pattern: phrase,
      weight: 5,
    });
  }

  return rules;
}

function buildRules() {
  const rules = [];
  const idSeen = new Set();
  const counts = new Map();

  for (const definition of taxonomyV3Source) {
    const categoryRules = [
      ...buildRulesForLanguage(definition, 'zh-CN'),
      ...buildRulesForLanguage(definition, 'en-US'),
    ];

    if (categoryRules.length < MIN_RULES_PER_CATEGORY || categoryRules.length > MAX_RULES_PER_CATEGORY) {
      throw new Error(
        `Category ${definition.category} generated ${categoryRules.length} rules; expected ${MIN_RULES_PER_CATEGORY}-${MAX_RULES_PER_CATEGORY}`,
      );
    }

    counts.set(definition.category, categoryRules.length);

    for (const rule of categoryRules) {
      let id = `${TAXONOMY_V3_VERSION}-${rule.lang}-${rule.category}-${slugify(rule.pattern)}`;
      let suffix = 2;

      while (idSeen.has(id)) {
        id = `${TAXONOMY_V3_VERSION}-${rule.lang}-${rule.category}-${slugify(rule.pattern)}-${suffix}`;
        suffix += 1;
      }

      idSeen.add(id);
      rules.push({
        id,
        version: TAXONOMY_V3_VERSION,
        ...rule,
        enabled: true,
      });
    }
  }

  return { rules, counts };
}

function renderSql(rules, counts) {
  const lines = [
    '-- Refresh TaxonomyRule version v3 with an expanded fine-grained bilingual taxonomy.',
    '-- This file is generated by scripts/generate-taxonomy-v3.mjs.',
    '-- Execute against the mem9 database, for example:',
    '--   mysql -D mem9 < prisma/taxonomy-v3.sql',
    '',
    'SET NAMES utf8mb4;',
    '',
    'START TRANSACTION;',
    '',
    'DELETE FROM `TaxonomyRule`',
    `WHERE \`version\` = '${TAXONOMY_V3_VERSION}';`,
    '',
    'INSERT INTO `TaxonomyRule` (',
    '  `id`,',
    '  `version`,',
    '  `category`,',
    '  `label`,',
    '  `lang`,',
    '  `matchType`,',
    '  `pattern`,',
    '  `weight`,',
    '  `enabled`',
    ') VALUES',
  ];

  rules.forEach((rule, index) => {
    const suffix = index === rules.length - 1 ? ';' : ',';
    lines.push(
      `  ('${rule.id}', '${rule.version}', '${rule.category}', '${sqlEscape(rule.label)}', '${rule.lang}', '${rule.matchType}', '${sqlEscape(rule.pattern)}', ${rule.weight}, true)${suffix}`,
    );
  });

  lines.push(
    '',
    'SELECT `category`, `lang`, COUNT(*) AS rule_count',
    'FROM `TaxonomyRule`',
    `WHERE \`version\` = '${TAXONOMY_V3_VERSION}'`,
    'GROUP BY `category`, `lang`',
    'ORDER BY `category`, `lang`;',
    '',
    '-- Summary counts generated by script',
  );

  for (const [category, count] of counts.entries()) {
    lines.push(`-- ${category}: ${count}`);
  }

  lines.push('', 'COMMIT;', '');

  return `${lines.join('\n')}`;
}

function main() {
  const { rules, counts } = buildRules();
  const sql = renderSql(rules, counts);
  fs.writeFileSync(OUTPUT_PATH, sql, 'utf8');
  console.log(`Generated ${rules.length} rules into ${OUTPUT_PATH}`);

  for (const [category, count] of counts.entries()) {
    console.log(`${category}: ${count}`);
  }
}

main();
